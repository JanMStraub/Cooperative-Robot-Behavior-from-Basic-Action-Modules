class TestRefreshIndexMetadataPreservation:
    """refresh_index must not overwrite metadata on existing entries."""

    def test_existing_outcome_metadata_preserved(self):
        """Execution stats written via update_operation_metadata survive a refresh."""
        ops = [_make_mock_op("op_001", "move_to_coordinate")]
        indexer, _ = _make_indexer(ops)

        store = VectorStore()
        store.add_operation("op_001", _unit(4), {"name": "move_to_coordinate"})
        store.update_operation_metadata("op_001", {
            "execution_count": 42,
            "last_outcome": "success",
        })

        # Now add all context docs so nothing new is embedded
        for ctx_id in CONTEXT_DOC_IDS:
            store.add_operation(ctx_id, _unit(4), {"name": ctx_id})

        indexer.refresh_index(store, save=False)

        # The original operation's metadata must be untouched
        op_entry = store.get_operation("op_001")
        assert op_entry is not None
        assert op_entry["metadata"]["execution_count"] == 42
        assert op_entry["metadata"]["last_outcome"] == "success"

    def test_new_op_does_not_affect_existing_metadata(self):
        """Adding a new operation must not change metadata of existing entries."""
        ops = [
            _make_mock_op("op_001", "move_to_coordinate"),
            _make_mock_op("op_002", "grasp_object"),
        ]
        indexer, _ = _make_indexer(ops)

        store = VectorStore()
        store.add_operation("op_001", _unit(4), {"name": "move_to_coordinate"})
        store.update_operation_metadata("op_001", {"execution_count": 7})
        for ctx_id in CONTEXT_DOC_IDS:
            store.add_operation(ctx_id, _unit(4), {"name": ctx_id})

        indexer.refresh_index(store, save=False)

        op_entry = store.get_operation("op_001")
        assert op_entry is not None
        assert op_entry["metadata"]["execution_count"] == 7


