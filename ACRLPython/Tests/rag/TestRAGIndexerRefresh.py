class TestRefreshIndexWorkflows:
    """refresh_index handles workflow patterns alongside operations."""

    def test_new_workflow_added(self):
        """A workflow not yet in the store is added on refresh."""
        ops = []
        workflows = [_make_mock_workflow("wf_001", "handoff_pattern")]
        indexer, _ = _make_indexer(ops, workflows)

        store = VectorStore()
        indexer.refresh_index(store, save=False)

        assert "wf_001" in store.operation_ids

    def test_existing_workflow_not_duplicated(self):
        """A workflow already in the store is not added again."""
        ops = []
        workflows = [_make_mock_workflow("wf_001", "handoff_pattern")]
        indexer, emb_gen = _make_indexer(ops, workflows)

        store = VectorStore()
        store.add_operation("wf_001", _unit(4), {"name": "handoff_pattern"})
        for ctx_id in CONTEXT_DOC_IDS:
            store.add_operation(ctx_id, _unit(4), {"name": ctx_id})

        indexer.refresh_index(store, save=False)

        assert store.operation_ids.count("wf_001") == 1
        emb_gen.generate_embeddings.assert_not_called()


