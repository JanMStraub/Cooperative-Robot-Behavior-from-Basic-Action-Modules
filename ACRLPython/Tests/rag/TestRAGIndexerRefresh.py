# Persistence (save/no-save)
# ---------------------------------------------------------------------------

class TestRefreshIndexSave:
    """refresh_index respects the save= parameter and RAG_AUTO_SAVE_INDEX config."""

    def test_save_true_calls_store_save(self):
        """save=True triggers VectorStore.save() when RAG_AUTO_SAVE_INDEX is True."""
        ops = [_make_mock_op("op_001", "move_to_coordinate")]
        indexer, _ = _make_indexer(ops)
        store = VectorStore()

        with patch("rag.Indexer.RAG_AUTO_SAVE_INDEX", True), \
             patch.object(store, "save") as mock_save:
            indexer.refresh_index(store, save=True)

        mock_save.assert_called_once()

    def test_save_false_does_not_call_store_save(self):
        """save=False must never call VectorStore.save()."""
        ops = [_make_mock_op("op_001", "move_to_coordinate")]
        indexer, _ = _make_indexer(ops)
        store = VectorStore()

        with patch("rag.Indexer.RAG_AUTO_SAVE_INDEX", True), \
             patch.object(store, "save") as mock_save:
            indexer.refresh_index(store, save=False)

        mock_save.assert_not_called()

    def test_auto_save_false_prevents_save(self):
        """When RAG_AUTO_SAVE_INDEX=False, save is not called even with save=True."""
        ops = [_make_mock_op("op_001", "move_to_coordinate")]
        indexer, _ = _make_indexer(ops)
        store = VectorStore()

        with patch("rag.Indexer.RAG_AUTO_SAVE_INDEX", False), \
             patch.object(store, "save") as mock_save:
            indexer.refresh_index(store, save=True)

        mock_save.assert_not_called()


